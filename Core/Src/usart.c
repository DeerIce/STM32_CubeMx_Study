//void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){} 
void USER_UART_IRQHandler(UART_HandleTypeDef *huart)
{
    if(huart==&huart1)// 判断是否是串口1
    {	
        if(RESET != __HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE))// 判断是否是空闲中断
        {
            __HAL_UART_CLEAR_IDLEFLAG(&huart1);// 清除空闲中断标志（否则会一直不断进入中断）  
            USAR_UART_IDLECallback(&huart1);//调用中断处理函数
        }
	}
}

/*数据处理函数*/
void USAR_UART_IDLECallback(UART_HandleTypeDef *huart)
{
	// 暂时关闭DMA
    //HAL_UART_DMAStop(&huart1);
	HAL_UART_DMAPause(&huart1);
	
	//清除DMA标志位
	__HAL_DMA_RESET_HANDLE_STATE(&hdma_usart1_rx);

    // 收到的数据长度=要接收的长度-待发送数据长度
    uint8_t data_length  = huart1.RxXferSize - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
    
	// 测试函数：将接收到的数据打印出去
	HAL_UART_Transmit(&huart1,arr,sizeof(arr),0x200);
    HAL_UART_Transmit(&huart1,RxMsg,data_length,0x200);

	// 清零接收缓冲区
    memset(RxMsg,0,data_length);
    data_length = 0;
    
    // 重启DMA接收 每次255字节数据
	HAL_UART_DMAResume(&huart1);
    HAL_UART_Receive_DMA(&huart1,(uint8_t*)RxMsg,sizeof(RxMsg));
}

